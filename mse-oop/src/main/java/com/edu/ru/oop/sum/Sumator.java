package com.edu.ru.oop.sum;

public class Sumator {

	/**
	 * Sums two numbers.
	 * 
	 * @param i
	 *            the first number
	 * @param j
	 *            the second number
	 * @return the sum of the two parameters.
	 */
	public static int sum(int i, int j) {
		return i + j;
	}

}
